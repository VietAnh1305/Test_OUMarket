package com.tester.pojo.sub;

/**
 *
 * @author LENOVO
 */
public class SubProduct {
    private String id;
    private String name;
    private float price;

    public SubProduct() {
    }

    public SubProduct(String id, String name, float price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }
    
    
    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the price
     */
    public float getPrice() {
        return price;
    }

    /**
     * @param price the price to set
     */
    public void setPrice(float price) {
        this.price = price;
    }
    
}
